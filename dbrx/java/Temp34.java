import javax.servlet.http.HttpServletRequest;
import org.springframework.web.context.request.ServletWebRequest;
import org.springframework.web.context.request.WebRequest;

public class Example {
    public static void main(String[] args) {
        // Assume we have a WebRequest object named messageContext
        WebRequest messageContext = new ServletWebRequest(new MockHttpServletRequest());

        HttpServletRequest httpServletRequest = ((ServletWebRequest)messageContext).getNativeRequest(HttpServletRequest.class);

        // Now you can use httpServletRequest as needed
        System.out.println("Request method: " + httpServletRequest.getMethod());
    }
}

// Mock class to simulate HttpServletRequest
