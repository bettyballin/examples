from cryptography.hazmat.primitives import hashes
from cryptography.hazmat.primitives.ciphers.aead import AESGCM
from cryptography.hazmat.primitives.padding import PKCS7

# Assuming `ciphertext` is your encrypted data and it has been padded with 
# PKCS#5 padding.
ciphertext = b'\x00\x01\x02\x03\x04\x05\x06\x07\x08\x09\x0a\x0b\x0c\x0d\x0e\x0f\x10\x11\x12\x13\x14\x15\x16\x17\x18\x19\x1a\x1b\x1c\x1d\x1e\x1f'
key = b'\x00\x01\x02\x03\x04\x05\x06\x07\x08\x09\x0a\x0b\x0c\x0d\x0e\x0f\x10\x11\x12\x13\x14\x15\x16\x17\x18\x19\x1a\x1b\x1c\x1d\x1e\x1f'

aesgcm = AESGCM(key)
padder = PKCS7(hashes.SHA256())
unpadder = padder.unpad(aesgcm.decrypt(b'\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00', ciphertext, None))
print(unpadder)