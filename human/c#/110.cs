using System;

[Flags]
public enum AccessMask : uint
{
    DELETE = 0x00010000,
    READ_CONTROL = 0x00020000,
    WRITE_DAC = 0x00040000,
    WRITE_OWNER = 0x00080000,
    SYNCHRONIZE = 0x00100000,

    GENERIC_READ = 0x80000000,
    GENERIC_WRITE = 0x40000000,
    GENERIC_EXECUTE = 0x20000000,
    GENERIC_ALL = 0x10000000,

    FILE_GENERIC_READ = GENERIC_READ,
    FILE_GENERIC_WRITE = GENERIC_WRITE,
    FILE_GENERIC_EXECUTE = GENERIC_EXECUTE,
    FILE_GENERIC_ALL = GENERIC_ALL,

    FILE_READ_DATA = 0x00000001,
    FILE_WRITE_DATA = 0x00000002,
    FILE_APPEND_DATA = 0x00000004,
    FILE_READ_EA = 0x00000008,
    FILE_WRITE_EA = 0x00000010,
    FILE_EXECUTE = 0x00000020,
    FILE_DELETE_CHILD = 0x00000040,
    FILE_READ_ATTRIBUTES = 0x00000080,
    FILE_WRITE_ATTRIBUTES = 0x00000100
}

class Program
{
    static void Main()
    {
        AccessMask desiredAccess = AccessMask.DELETE | AccessMask.READ_CONTROL;
        Console.WriteLine(desiredAccess);
    }
}