(async () => {
  const subtle = window.crypto.subtle;

  // Generate a 256-bit AES key
  const key = await subtle.importKey(
    'raw',
    new Uint8Array([
      0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x10, 0x11,
      0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x20, 0x21, 0x22, 0x23,
      0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x30, 0x31,
    ]),
    {
      name: 'AES-CBC',
    },
    false,
    ['encrypt']
  );

  // Generate an IV for CBC mode
  const iv = new Uint8Array([
    0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x10, 0x11,
    0x12, 0x13, 0x14, 0x15,
  ]);

  const textEncoder = new TextEncoder('utf-8');
  const encodedMessage = textEncoder.encode('Hello, World!');

  const encryptedMessage = await subtle.encrypt(
    {
      name: 'AES-CBC',
      iv: iv,
    },
    key,
    encodedMessage
  );

  console.log(encryptedMessage);
})();