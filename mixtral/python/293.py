from Crypto.Signature import DSS
from Crypto.Hash import SHA3_256
from Crypto.PublicKey import ECC

message = bytearray([0x12, 0x34, 0x56, 0x78])  # Replace with your message
private_key_buffer = bytearray([0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x20, 0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x30, 0x31, 0x32])  # Replace with your private key

# Create a private key object
sk = ECC.construct(curve='P-256', d=int.from_bytes(private_key_buffer, 'big'))

# Sign the message
h = SHA3_256.new()
h.update(message)
signature = sk.sign_digest(h.digest())

print(signature.hex())