public class Temp1221 {
    public static void main(String[] args) {
        int N = 10; // example value for N
        int MINIMUM_LENGTH = 1; // example value for MINIMUM_LENGTH
        
        for(int length = N; length >= MINIMUM_LENGTH; --length){
            System.out.println("Current length: " + length); // example operation
        }
    }
}